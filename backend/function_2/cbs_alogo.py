def add_temporary_node(graph, point):
    """
    ‡πÄ‡∏û‡∏¥‡πà‡∏°‡πÇ‡∏´‡∏ô‡∏î‡∏ä‡∏±‡πà‡∏ß‡∏Ñ‡∏£‡∏≤‡∏ß‡∏•‡∏á‡πÉ‡∏ô‡∏Å‡∏£‡∏≤‡∏ü NetworkX ‡πÅ‡∏•‡∏∞‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠‡∏Å‡∏±‡∏ö‡πÇ‡∏´‡∏ô‡∏î‡∏ó‡∏µ‡πà‡πÉ‡∏Å‡∏•‡πâ‡∏ó‡∏µ‡πà‡∏™‡∏∏‡∏î
    
    Args:
        graph (nx.Graph): ‡∏Å‡∏£‡∏≤‡∏ü NetworkX
        point (tuple): ‡∏û‡∏¥‡∏Å‡∏±‡∏î‡∏Ç‡∏≠‡∏á‡∏à‡∏∏‡∏î‡πÉ‡∏´‡∏°‡πà (lat, lon)
    
    Returns:
        nx.Graph: ‡∏™‡∏≥‡πÄ‡∏ô‡∏≤‡∏Ç‡∏≠‡∏á‡∏Å‡∏£‡∏≤‡∏ü‡∏ó‡∏µ‡πà‡∏°‡∏µ‡πÇ‡∏´‡∏ô‡∏î‡∏ä‡∏±‡πà‡∏ß‡∏Ñ‡∏£‡∏≤‡∏ß‡πÉ‡∏´‡∏°‡πà
    """


    # ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏™‡∏≥‡πÄ‡∏ô‡∏≤‡∏Ç‡∏≠‡∏á‡∏Å‡∏£‡∏≤‡∏ü
    temp_graph = graph.copy()
    
    # ‡∏ñ‡πâ‡∏≤‡∏à‡∏∏‡∏î‡∏ô‡∏µ‡πâ‡∏°‡∏µ‡∏≠‡∏¢‡∏π‡πà‡πÉ‡∏ô‡∏Å‡∏£‡∏≤‡∏ü‡∏≠‡∏¢‡∏π‡πà‡πÅ‡∏•‡πâ‡∏ß ‡πÉ‡∏´‡πâ‡∏™‡πà‡∏á‡∏Ñ‡∏∑‡∏ô‡∏Å‡∏£‡∏≤‡∏ü‡∏ï‡∏≤‡∏°‡πÄ‡∏î‡∏¥‡∏°
    if point in temp_graph.nodes():
        return temp_graph


    # ‡∏´‡∏≤‡πÇ‡∏´‡∏ô‡∏î‡∏ó‡∏µ‡πà‡πÉ‡∏Å‡∏•‡πâ‡πÄ‡∏Ñ‡∏µ‡∏¢‡∏á‡∏ó‡∏µ‡πà‡∏™‡∏∏‡∏î‡πÇ‡∏î‡∏¢‡πÉ‡∏ä‡πâ‡∏£‡∏∞‡∏¢‡∏∞‡∏ó‡∏≤‡∏á geodesic (‡∏£‡∏∞‡∏¢‡∏∞‡∏ó‡∏≤‡∏á‡∏ö‡∏ô‡∏ú‡∏¥‡∏ß‡πÇ‡∏•‡∏Å)
    nearest = min(temp_graph.nodes(), key=lambda node: geodesic(point, node).meters)
    # nearest = min(temp_graph.nodes(), key=lambda node: heuristic(point, node).meters)


    # ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏£‡∏∞‡∏¢‡∏∞‡∏ó‡∏≤‡∏á‡πÑ‡∏õ‡∏¢‡∏±‡∏á‡πÇ‡∏´‡∏ô‡∏î‡∏ó‡∏µ‡πà‡πÉ‡∏Å‡∏•‡πâ‡∏ó‡∏µ‡πà‡∏™‡∏∏‡∏î
    distance = geodesic(point, nearest).meters


    # ‡πÄ‡∏û‡∏¥‡πà‡∏°‡πÇ‡∏´‡∏ô‡∏î‡πÉ‡∏´‡∏°‡πà‡πÅ‡∏•‡∏∞‡πÄ‡∏™‡πâ‡∏ô‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°
    temp_graph.add_node(point)
    temp_graph.add_edge(point, nearest, weight=distance, length=distance)

    return temp_graph




